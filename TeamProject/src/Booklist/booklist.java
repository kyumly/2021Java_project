    private ArrayList<Integer> bookid(){
        

        ArrayList<Integer> idlist = new ArrayList<>();
        try{
            String SQL = "SELECT distinct movieid from book where userid = ?";
            db.pstmt = db.conn.prepareStatement(SQL);
            db.pstmt.setString(1, members.getUserId());
            db.rs = db.pstmt.executeQuery();
            
            while(db.rs.next()){
                idlist.add(db.rs.getInt(1));
                
            }
            return idlist;
        }catch(Exception e){
            System.out.println("SQLException : " + e.getMessage());
        }return idlist;
    }
    private String js(int id){
        String jsstr = "";
        try{
            String SQL = "SELECT moviejs from bookjs where movieid = ?";
            db.pstmt = db.conn.prepareStatement(SQL);
            db.pstmt.setInt(1, id);
            db.rs = db.pstmt.executeQuery();
            
            while(db.rs.next()){
                jsstr += db.rs.getString(1) + " ";
                
            }
            return jsstr;
        }catch(Exception e){
            System.out.println("SQLException : " + e.getMessage());
        }return jsstr;
    }
    
    private void movieinfo(int id){
        String SQL = "SELECT distinct moviename, b.movieday, b.movietime from book b, movie mv"
                + " where mv.movieid = b.movieid and userid = ? and b.movieid = ?";
        try{
            db.pstmt = db.conn.prepareStatement(SQL);
            db.pstmt.setString(1, members.getUserId());
            db.pstmt.setInt(2, id);
            db.rs = db.pstmt.executeQuery();
            while(db.rs.next()){
                int r;
                r = booktable.getRowCount();
                for(int i=0; i<r; i++){
                    if(booktable.getValueAt(i,0)==null){
                        r = i;
                        break;
                    }
                }
                booktable.setValueAt(db.rs.getString(1),r,0);
                booktable.setValueAt(db.rs.getString(2),r,1);
                booktable.setValueAt(db.rs.getString(3),r,2);
            }
        }catch(Exception e){
            System.out.println("SQLException : " + e.getMessage());
        }
    }
    
