    private void sel(String day, String time){
        String SQL = "select movieid, bookcnt from book where movieday = ? and movietime = ?";
        try{
            db.pstmt = db.conn.prepareStatement(SQL);
            db.pstmt.setString(1, day);
            db.pstmt.setString(2, time);
            db.rs = db.pstmt.executeQuery();
            if(db.rs.next()){
                movieid = db.rs.getString(1);
                bookcnt = db.rs.getInt(2);
            }
        }catch(Exception e){
            System.out.println("SQLException : " + e.getMessage());
        }
    }
    private void delbook(String day, String time){
        String SQL = "delete from book where movieday = ? and movietime = ?";
        try{
            db.pstmt = db.conn.prepareStatement(SQL);
            db.pstmt.setString(1, day);
            db.pstmt.setString(2, time);
            db.pstmt.executeUpdate();
        }catch (Exception e) {
                    System.out.println("SQLException : " + e.getMessage());
        }
    }
    private void deljs(String id){
        String SQL = "delete from bookjs where userid = ? and movieid = ?";
        try{
            db.pstmt = db.conn.prepareStatement(SQL);
            db.pstmt.setString(1, members.getUserId());
            db.pstmt.setString(2, id);
            db.pstmt.executeUpdate();
        }catch (Exception e) {
                    System.out.println("SQLException : " + e.getMessage());
        }
    }
    private void up(String day, String time, int cnt){
        String SQL = "update movie set moviecnt = moviecnt+? where movieday = ? and movietime = ?";
        try{
            db.pstmt = db.conn.prepareStatement(SQL);
            db.pstmt.setInt(1, cnt);
            db.pstmt.setString(2, day);
            db.pstmt.setString(3, time);
            db.pstmt.executeUpdate();
        }catch (Exception e) {
                    System.out.println("SQLException : " + e.getMessage());
        }
    }

